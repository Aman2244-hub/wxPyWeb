<ul>
<li><i>(8-Sept-2007)</i> Yes, the <b>mail lists are down</b>.  There were multiple disk failures in RAID5 array of the machine that <a href="http://dotsrc.org/">hosts</a> the mail lists.  The hardware has been replaced and data is being restored from tape backups.
     
<p><li><i>(8-Aug-2007)</i> <b>wxPython 2.8.4.2</b>has been <a href="download.php">released</a>.  This release includes several fixes and a few additions and improvements, as well as new build features for <a href="http://wiki.wxpython.org/InstallingOnUbuntuOrDebian">Debian/Ubuntu</a> and OS X.
See <a href="recentchanges.php">Recent Changes</a> for more details.
  
<p><li><i>(14-May-2007)</i> <b>wxPython 2.8.4.0</b> has been <a href="download.php">released</a>, with lots of updates, fixes and improvements.

<p><li><i>(22-Mar-2007)</i> <b>wxPython 2.8.3.0</b> has been released.   This release includes a number of bug fixes and also some new enhancements, including updates to the XRCed tool and the new InspectionTool.  

<p><li><i>(20-Jan-2007)</i> <b>wxPython 2.8.1.1</b> has been released.   This release adds a few minor enhancements and a number of bug fixes designed to further stabalize
the 2.8.x release series.

<p><li><i>(11-Dec-2006)</i> The wxPython reference docs have been put on a server that doesn't choke on large files like this one does.  You can now access the <a href="http://wxpython.wxcommunity.com/docs/api/"><b>API docs</b></a> at <a href="http://wxcommunity.com/">wxCommunity</a>.  Please update your bookmarks.  As always you can also <a href="download.php#documentation">download a tarball</a> containing the html files to access locally if you wish.

<p><li><i>(11-Dec-2006)</i> <b>wxPython 2.8.0.1</b> has been released.  This is the first in a new <a href="http://wiki.wxpython.org/index.cgi/ReleaseSeries">stable release series</a>, and includes a few new widget classes and other enhancements since 2.7.2.  

<p><li><i>(7-Nov-2006)</i> <b>wxPython 2.7.2.0</b> has been released.  This is expected to be the last stepping stone in the path to the next stable release series, 2.8.x.  We're driving full speed ahead in order to get 2.8.0 included with OSX 10.5, and so far we are very close to being on schedule.  This release has some house-keeping style changes, as well as some user-contributed patches and also the usual crop of bug fixes.  

<p><li><i>(26-Oct-2006)</i> <b>wxPython 2.7.1.3</b> has been released.  This is another quick turn-around bug-fix release. 

<p><li><i>(21-Oct-2006)</i> <b>wxPython 2.7.1.2</b> has been released.  This is bug-fix release meant to resolve some issues discovered in the 2.7.1.1 release.

<p><li><i>(18-Oct-2006)</i> <b>wxPython 2.7.1.1</b> has been released.  This is the first official release of the 2.7 development branch.  Lots of new features have been added, and some existing things have changed (but mostly in backwards compatible ways.) 

<p><li><i>(15-July-2006)</i> <b>wxPython 2.6.3.3</b> has been <a href="download-2.6.3.3.php">released</a>. This is mostly a bugfix release.  

<p><li>A snapshot of the new style wxPython reference docs is available <a href="docs/api/">here</a>.  While most of the content is not yet present, the docs are still usable, and in fact helpful since they already accurately document what classes and methods are present in wxPython and what the parameter names are.

<p><li>Robin's OSCON 2006 tutorial and presentation slides are <a href="presentations.php">available</a>.
</ul>
